public class Datatypes {
    public static void main(String[] args) {
        
        int intValue = 25;  
        long longValue = 100000L;  
        
        
        float floatValue = 12.5f;  
        double doubleValue = 15.99;  
        
        
        char charValue = 'A'; 

        
        boolean booleanValue = true;  
        
        
        String stringValue = "Hello, Java!";  
        
        
        System.out.println("Integer value (int): " + intValue);
        System.out.println("Long value (long): " + longValue);
        System.out.println("Float value (float): " + floatValue);
        System.out.println("Double value (double): " + doubleValue);
        System.out.println("Character value (char): " + charValue);
        System.out.println("Boolean value (boolean): " + booleanValue);
        System.out.println("String value (String): " + stringValue);
    }
}
